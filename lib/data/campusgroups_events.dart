/// importKey uses the stable CampusGroups event ID so re-imports always match.
Map<String, dynamic> _ev(
  String cgId,
  String title,
  String category,
  DateTime start,
  DateTime end,
  String locKey,
  int capacity,
) {
  final l = _loc[locKey]!;
  return {
    'title': title,
    'category': category,
    'startTime': Timestamp.fromDate(start),
    'endTime': Timestamp.fromDate(end),
    'description': title,
    'hostId': 'campusgroups_import',
    'hostName': 'NYIT CampusGroups',
    'vibeTags': <String>[],
    'locationName': l['name'],
    'locationKey': locKey,
    'locationLat': l['lat'],
    'locationLng': l['lng'],
    'capacity': capacity,
    'rsvpCount': 0,
    'waitlistCount': 0,
    'status': 'published',
    'reactions': <String, int>{},
    'isPinned': false,
    'postEventSummary': '',
    'imageUrl': '',
    'importKey': 'cg:$cgId',
    'createdAt': Timestamp.now(),
  };
}

